    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (validarCampos() && !txtId.getText().isEmpty()) {
            int id = Integer.parseInt(txtId.getText());
            String nombres = txtNombres.getText().trim();
            String apellidos = txtApellidos.getText().trim();
            String dni = txtDni.getText().trim();
            String telefono = txtTelefono.getText().trim();
            
            Cliente clienteActualizado = new Cliente(id, nombres, apellidos, telefono, dni);
            repo.actualizar(clienteActualizado);
            
            JOptionPane.showMessageDialog(this, "Cliente modificado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            actualizarTabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la tabla para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtId.getText().isEmpty()) {
             JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la tabla para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
             return;
        }
        
        int r = JOptionPane.showConfirmDialog(this, 
                "¿Está seguro de que desea eliminar a este cliente?", 
                "Confirmar Eliminación", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.WARNING_MESSAGE);
        
        if (r == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(txtId.getText());
            repo.eliminar(id);
            
            JOptionPane.showMessageDialog(this, "Cliente eliminado.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            actualizarTabla();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void jTableClientesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMousePressed
        int fila = jTableClientes.getSelectedRow();
        
        if (fila >= 0) {
            String id = tableModel.getValueAt(fila, 0).toString();
            String nombres = tableModel.getValueAt(fila, 1).toString();
            String apellidos = tableModel.getValueAt(fila, 2).toString();
            String dni = tableModel.getValueAt(fila, 3).toString();
            String telefono = tableModel.getValueAt(fila, 4).toString();
            
            txtId.setText(id);
            txtNombres.setText(nombres);
            txtApellidos.setText(apellidos);
            txtDni.setText(dni);
            txtTelefono.setText(telefono);
            
            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }//GEN-LAST:event_jTableClientesMousePressed

