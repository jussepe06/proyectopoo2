    /**
     * Prepara las columnas de la tabla.
     */
    private void configurarTabla() {
        tableModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Celdas no editables
            }
        };
        
        tableModel.addColumn("ID");
        tableModel.addColumn("Nombres");
        tableModel.addColumn("Apellidos");
        tableModel.addColumn("DNI");
        tableModel.addColumn("Teléfono");
        
        jTableClientes.setModel(tableModel); // 'jTableClientes' debe ser el nombre de tu tabla
    }

    /**
     * Limpia la tabla y la vuelve a llenar con datos del Repositorio.
     */
    private void actualizarTabla() {
        tableModel.setRowCount(0); 
        List<Cliente> clientes = repo.obtenerTodos();
        for (Cliente c : clientes) {
            Object[] fila = {
                c.getId(),
                c.getNombres(),
                c.getApellidos(),
                c.getDni(),
                c.getTelefono()
            };
            tableModel.addRow(fila);
        }
    }

    /**
     * Limpia todos los campos de texto y quita la selección.
     */
    private void limpiarCampos() {
        // Asegúrate que tus campos se llamen así
        // 'txtId' es el campo de texto que te dije que añadieras
        txtId.setText(""); 
        txtNombres.setText("");
        txtApellidos.setText("");
        txtDni.setText("");
        txtTelefono.setText("");
        jTableClientes.clearSelection();
        
        btnGuardar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }

    /**
     * Valida que los campos no estén vacíos.
     */
    private boolean validarCampos() {
        if (txtNombres.getText().trim().isEmpty() || 
            txtApellidos.getText().trim().isEmpty() || 
            txtDni.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nombres, Apellidos y DNI son obligatorios.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

